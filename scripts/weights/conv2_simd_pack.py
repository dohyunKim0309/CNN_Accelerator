def simd_decode(aport_25bit, x):
    """
    SIMD packing 결과를 받아 mul0, mul1 추출
    DSP의 25 × 18 signed multiplication 동작 시뮬레이션
    """
    # 25-bit signed 해석
    if aport_25bit & 0x1000000:
        aport_signed = aport_25bit - 0x2000000
    else:
        aport_signed = aport_25bit

    # x도 signed
    x_signed = x if x < 128 else x - 256  # int8

    # Multiplication
    p = aport_signed * x_signed  # signed

    # Extract mul0 (lower 17 bits as 17-bit signed)
    mul0_bits = p & 0x1FFFF
    mul0 = mul0_bits - 0x20000 if mul0_bits & 0x10000 else mul0_bits

    # Extract mul1 (bits [32:17] as 16-bit signed)
    mul1_bits = (p >> 17) & 0xFFFF
    mul1 = mul1_bits - 0x10000 if mul1_bits & 0x8000 else mul1_bits

    # Carry correction
    if mul0 < 0:
        mul1 += 1

    return mul0, mul1


# Verification
for w0 in range(-127, 128):  # -128 제외
    for w1 in range(-127, 128):
        for x in range(-128, 128):
            # Pack
            aport_int = w1 * (1 << 17) + w0
            aport_25 = aport_int & 0x01FFFFFF

            # Decode (DSP simulation)
            mul0, mul1 = simd_decode(aport_25, x & 0xFF)

            # Expected
            expected_mul0 = w0 * x
            expected_mul1 = w1 * x

            assert mul0 == expected_mul0, f"w0={w0} w1={w1} x={x}: mul0={mul0} expected={expected_mul0}"
            assert mul1 == expected_mul1, f"w0={w0} w1={w1} x={x}: mul1={mul1} expected={expected_mul1}"

print("Exhaustive verification passed: 254 × 254 × 256 cases")
